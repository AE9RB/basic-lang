pub mod COS {
    /*!
    ## COS(X)
    Returns the cosine of X in radians.
    ```text
    PRINT COS(0.123)
     0.99244505
    ```
    */
}

pub mod SIN {
    /*!
    ## SIN(X)
    Returns the sine of X in radians.
    ```text
    PRINT SIN(0.123)
     0.1226901
    ```
    */
}
